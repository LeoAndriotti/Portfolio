let idiomaAtual = localStorage.getItem("idioma") === "en" ? "en" : "pt";

function t(chave) {
    return I18N[idiomaAtual][chave] ?? I18N.pt[chave] ?? chave;
}

function aplicarIdioma(lang) {
    idiomaAtual = lang === "en" ? "en" : "pt";
    localStorage.setItem("idioma", idiomaAtual);

    document.documentElement.lang = idiomaAtual === "en" ? "en" : "pt-BR";

    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const chave = el.getAttribute("data-i18n");
        if (!chave) return;
        el.textContent = t(chave);
    });

    document.querySelectorAll("[data-i18n-html]").forEach((el) => {
        const chave = el.getAttribute("data-i18n-html");
        if (!chave) return;
        el.innerHTML = t(chave);
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
        const chave = el.getAttribute("data-i18n-placeholder");
        if (!chave) return;
        el.placeholder = t(chave);
    });

    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
        const chave = el.getAttribute("data-i18n-aria");
        if (!chave) return;
        el.setAttribute("aria-label", t(chave));
    });

    document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
        const chave = el.getAttribute("data-i18n-alt");
        if (!chave) return;
        el.alt = t(chave);
    });

    const titulo = document.querySelector("title");
    if (titulo) titulo.textContent = t("meta.title");

    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.content = t("meta.description");

    const ogTitle = document.querySelector('meta[property="og:title"]');
    if (ogTitle) ogTitle.content = t("meta.ogTitle");

    const ogDesc = document.querySelector('meta[property="og:description"]');
    if (ogDesc) ogDesc.content = t("meta.ogDescription");

    const formSubject = document.getElementById("form-subject");
    if (formSubject) formSubject.value = t("contact.subject");

    const btnIdioma = document.getElementById("btn-idioma");
    if (btnIdioma) {
        btnIdioma.textContent = idiomaAtual === "pt" ? "EN" : "PT";
        btnIdioma.setAttribute("aria-label", t("aria.langSwitch"));
    }

    document.dispatchEvent(
        new CustomEvent("i18n:change", { detail: { lang: idiomaAtual } })
    );
}

function alternarIdioma() {
    aplicarIdioma(idiomaAtual === "pt" ? "en" : "pt");
}

window.PortfolioI18n = { t, aplicarIdioma, alternarIdioma, getLang: () => idiomaAtual };

function iniciarI18n() {
    aplicarIdioma(idiomaAtual);

    const btnIdioma = document.getElementById("btn-idioma");
    btnIdioma?.addEventListener("click", alternarIdioma);
}

if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", iniciarI18n);
} else {
    iniciarI18n();
}
